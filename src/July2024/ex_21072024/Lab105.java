package July2024.ex_21072024;

public class Lab105 {
    public static void main(String[] args) {
        // Loop - while and doiwhile

        // while - A, B, C
        //    A - Init
        //    B - Condition
        //    C - Increment / Decrement

        int i = 0;  // A
        while(i<=10){  // B
            System.out.println(i);
            i++;  // C
        }

        for (int j = 0; j < 5; j++) {
            System.out.println(j);
        }
    }
}
