package ex_21072024;

public class Lab110 {
    public static void main(String[] args) {
       // while, do while -?
        // do while - while -> 1 minor change

        // while loop
        //  int -> condition -> body -> increment / decrement

        // Do while loop
        // int -> body -> increment / decrement -> condition
        // Do always run 1 time
        int a = 0;
        do {
            System.out.println(a);
            a++;
        } while(a < 10);

    }
}
